package by.skyperdyay.exchange.api;

import java.time.LocalDate;

/**
 * Интерфейс для провайдеров курсов обмена валют.
 * Реализации получают курсы обмена из внешних API и возвращают их в виде объектов данных.
 * Планировщик отвечает за оркестрацию вызовов всех реализаций.
 */
public interface CurrencyExchangeRatesProvider {

    /**
     * Уникальный код, идентифицирующий данного провайдера (например, "nbrb-byn", "cbr-rub").
     * Используется для отслеживания статуса выполнения и целей аудита.
     *
     * @return код провайдера
     */
    String getProviderCode();

    /**
     * Код базовой валюты, для которой данный провайдер получает курсы обмена.
     * Например: "BYN" для провайдера белорусского рубля, "RUB" для провайдера российского рубля.
     *
     * @return код базовой валюты (ISO 4217)
     */
    String getBaseCurrencyCode();

    /**
     * Сохраняет курсы обмена для базовой валюты с указанной даты.
     * Если fromDate равен null, провайдер должен получить все доступные текущие курсы.
     * <p>
     * Реализация провайдера отвечает за:
     * 1. Выполнение HTTP-запроса к внешнему API
     * 2. Разбор ответа
     * 3. Преобразование в объекты ExchangeRateData
     * 4. Вызов ExchangeRatesApiService для сохранения данных
     *
     * @param fromDate дата последнего успешного получения данных (null для начального получения)
     */
    void fetchRatesSince(LocalDate fromDate);
}
